    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {

        String username = request.getParameter("username");
        String password = request.getParameter("password");

        // Gọi phương thức đăng nhập từ Database và lấy thông tin User
        User user = Database.getUserByUsernameAndPassword(username, password);

        if (user != null) { // Kiểm tra nếu đăng nhập thành công
            HttpSession session = request.getSession();
            session.setAttribute("user", user); // Lưu user vào session

            // Tạo cookie đăng nhập
            Cookie loginCookie = new Cookie("username", user.getUsername());
            loginCookie.setMaxAge(60 * 60 * 24); // Cookie tồn tại 1 ngày
            loginCookie.setPath("/"); // Áp dụng cho toàn bộ ứng dụng
            response.addCookie(loginCookie);

            // Chuyển hướng đến DashboardServlet
            response.sendRedirect("DashboardServlet");

        } else {
            // Nếu sai tài khoản/mật khẩu, chuyển hướng về trang login với thông báo lỗi
            RequestDispatcher rd = getServletContext().getRequestDispatcher("/index.html");
            PrintWriter out = response.getWriter();
            out.println("<font color=red>Either user name or password is wrong.</font>");
            rd.include(request, response);
        }
    }

